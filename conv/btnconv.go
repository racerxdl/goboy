type pixelButtonToLocalButton struct {
	win *pixelgl.Window
}

func MakePixelGLButtonConverter(win *pixelgl.Window) localimg.IButton {
	return &pixelButtonToLocalButton{win: win}
}

func (p *pixelButtonToLocalButton) Pressed(button localimg.Button) bool {
	return p.win.Pressed(localToPixelgl[button])
}
